<script>
    function validateForm(event) {
        event.preventDefault();
        let isValid = true;

        const nameField = document.querySelector('input[name="your-name"]');
        const emailField = document.querySelector('input[name="your-email"]');
        const telField = document.querySelector('input[name="your-tel"]');
        const quizField = document.querySelector('input[name="quiz"]');

        if (!nameField.value.trim()) {
            document.getElementById('error-name').style.display = 'block';
            isValid = false;
        } else {
            document.getElementById('error-name').style.display = 'none';
        }

        if (!emailField.value.trim()) {
            document.getElementById('error-email').style.display = 'block';
            isValid = false;
        } else {
            document.getElementById('error-email').style.display = 'none';
        }

        if (!telField.value.trim()) {
            document.getElementById('error-tel').style.display = 'block';
            isValid = false;
        } else {
            document.getElementById('error-tel').style.display = 'none';
        }

        if (!quizField.value.trim()) {
            document.getElementById('error-quiz').style.display = 'block';
            isValid = false;
        } else {
            document.getElementById('error-quiz').style.display = 'none';
        }

        if (isValid) {
            event.target.submit();
        }
    }
</script>